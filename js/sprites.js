// Draws a palette-index grid onto the canvas at (originX, originY), each cell
// rendered as a pixelSize x pixelSize square. 0 cells are skipped (transparent).
Sprites.drawPixelGrid = function (ctx, grid, originX, originY, pixelSize, palette) {
  for (var row = 0; row < grid.length; row++) {
    var cols = grid[row];
    for (var col = 0; col < cols.length; col++) {
      var value = cols[col];
      if (value === 0) continue;
      ctx.fillStyle = palette[value];
      ctx.fillRect(
        Math.round(originX + col * pixelSize),
        Math.round(originY + row * pixelSize),
        pixelSize,
        pixelSize
      );
    }
  }
};
